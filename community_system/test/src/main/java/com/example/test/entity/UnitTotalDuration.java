package com.example.test.entity;

public class UnitTotalDuration {
    private int unitId;
    private String unitName;
    private double unitTotHour;

    public int getUnitId() {
        return unitId;
    }

    public void setUnitId(int unitId) {
        this.unitId = unitId;
    }

    public String getUnitName() {
        return unitName;
    }

    public void setUnitName(String unitName) {
        this.unitName = unitName;
    }

    public double getUnitTotHour() {
        return unitTotHour;
    }

    public void setUnitTotHour(double unitTotHour) {
        this.unitTotHour = unitTotHour;
    }
}
